const DAYnum = parseInt(prompt("Enter Day Number: "));

if (DAYnum == 1) {
    document.write("Monday")
}

else if (DAYnum == 2) {
    document.write("Tuesday")
}

else if (DAYnum == 3) {
    document.write("wednesday")
}

else if (DAYnum == 4) {
    document.write("Thursday")
}

else if (DAYnum == 5) {
    document.write("Friday")
}
else if (DAYnum == 6) {
    document.write("Saturday")
}
else if (DAYnum == 7) {
    document.write("Sunday")
}
else {
    document.write(" Day Not Match")
}